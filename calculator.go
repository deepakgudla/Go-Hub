package main

import "fmt"

func main() {
	a := 10
	b := 5

	
	sum := a + b
	fmt.Printf("Sum: %d\n", sum)

	
	diff := a - b
	fmt.Printf("Difference: %d\n", diff)

	
	product := a * b
	fmt.Printf("Product: %d\n", product)

	
	quotient := a / b
	fmt.Printf("Quotient: %d\n", quotient)

	
	remainder := a % b
	fmt.Printf("Remainder: %d\n", remainder)
}
